    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String ad = null;
        String id = username.getText();
        String sifre = password.getText();
        int onay = 0;
        if (id.length() >= 2 && sifre.length() >= 2) {
            try {
                Connection myConn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/musicapidb", "root", "1234");
                Statement myStat = (Statement) myConn.createStatement();
                ResultSet myRs = myStat.executeQuery("select * from kullanici");
                while (myRs.next()) {
                    
                    if (myRs.getString("kullaniciid").equals(id) && myRs.getString("sifre").equals(sifre)) {
                        JOptionPane.showMessageDialog(null, "Giris Yapildi !");
                        ad = myRs.getString("kullaniciad");
                        onay = 1;
                    }
                }
                if(onay == 0){
                    JOptionPane.showMessageDialog(null,"kullanici ad/sifre kutularini kontrol et !");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Data Base Hatasi : "+e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null,"ad/sifre kutularini kontrol et !");
        }
        if(onay == 1){
            username.setText("");
            password.setText("");
            logisc.setVisible(false);
            AnaProgram anaprog = new AnaProgram(ad,sifre,id);
            anaprog.al(anaprog,logisc);
            anaprog.setVisible(true);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

